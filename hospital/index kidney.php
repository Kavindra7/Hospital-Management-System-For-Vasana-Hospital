                <h2 style="color:blue;"><b>The surgery</b></h2>
                <p>Most kidney transplant surgeries are done laparoscopically. A laparoscopic surgery is a new surgery method that uses very small cuts on the body and a thin lighted tube to look inside the body. In a laparoscopic kidney donor surgery, the surgeon makes small cuts on the donor's stomach and the kidney is removed through an incision just big enough for it to fit through. This operation takes 2-3 hours and a kidney donor usually spends 1-3 days in the hospital recovering. The recovery period after laparoscopic surgery is much shorter than after a traditional open surgery. There are also fewer complications with laparoscopic surgery.</p>
